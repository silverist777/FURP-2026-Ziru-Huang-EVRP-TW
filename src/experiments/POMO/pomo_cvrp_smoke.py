"""Tiny RL4CO POMO smoke test for randomly generated CVRP instances.

This verifies the intended infrastructure path:

1. CVRP instances are generated by RL4CO, not loaded from a file.
2. A small POMO model can train for a few batches.
3. The trained model can run a multi-start CVRP inference pass.
"""

from __future__ import annotations

import argparse
import os
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parents[3]
CACHE_ROOT = REPO_ROOT / ".cache" / "pomo_cvrp_smoke"
os.environ.setdefault("MPLCONFIGDIR", str(CACHE_ROOT / "matplotlib"))
os.environ.setdefault("WANDB_DISABLED", "true")
CACHE_ROOT.mkdir(parents=True, exist_ok=True)
(CACHE_ROOT / "matplotlib").mkdir(parents=True, exist_ok=True)

import torch
from rl4co.envs import CVRPEnv
from rl4co.models.zoo.pomo import POMO
from rl4co.utils.ops import unbatchify
from rl4co.utils.trainer import RL4COTrainer


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Train and infer a tiny POMO model on randomly generated CVRP data."
    )
    parser.add_argument("--num-loc", type=int, default=20, help="Number of customers.")
    parser.add_argument("--batch-size", type=int, default=16)
    parser.add_argument("--train-data-size", type=int, default=32)
    parser.add_argument("--val-data-size", type=int, default=16)
    parser.add_argument("--test-data-size", type=int, default=16)
    parser.add_argument("--infer-batch-size", type=int, default=4)
    parser.add_argument("--max-epochs", type=int, default=1)
    parser.add_argument("--seed", type=int, default=1234)
    parser.add_argument("--lr", type=float, default=1e-4)
    parser.add_argument(
        "--device",
        choices=("auto", "cpu", "cuda"),
        default="auto",
        help="Device to use. auto prefers CUDA when available.",
    )
    return parser.parse_args()


def choose_device(requested: str) -> str:
    if requested == "auto":
        return "cuda" if torch.cuda.is_available() else "cpu"
    if requested == "cuda" and not torch.cuda.is_available():
        raise RuntimeError("CUDA was requested, but torch.cuda.is_available() is false.")
    return requested


def main() -> None:
    args = parse_args()
    torch.manual_seed(args.seed)
    device = choose_device(args.device)

    print("RL4CO POMO CVRP smoke")
    print("=====================")
    print(f"torch: {torch.__version__}")
    print(f"cuda_available: {torch.cuda.is_available()}")
    print(f"device: {device}")
    if device == "cuda":
        print(f"cuda_device_name: {torch.cuda.get_device_name(0)}")
    print(f"num_loc: {args.num_loc}")
    print("data_source: RL4CO CVRPEnv generator (random, no imported dataset)")

    env = CVRPEnv(generator_params={"num_loc": args.num_loc})
    sample_td = env.generator([args.infer_batch_size])
    print(
        "generated_batch_shapes: "
        f"depot={tuple(sample_td['depot'].shape)}, "
        f"locs={tuple(sample_td['locs'].shape)}, "
        f"demand={tuple(sample_td['demand'].shape)}"
    )

    model = POMO(
        env,
        num_augment=1,
        policy_kwargs={
            "embed_dim": 64,
            "num_encoder_layers": 2,
            "num_heads": 4,
            "feedforward_hidden": 128,
        },
        batch_size=args.batch_size,
        val_batch_size=args.batch_size,
        test_batch_size=args.batch_size,
        train_data_size=args.train_data_size,
        val_data_size=args.val_data_size,
        test_data_size=args.test_data_size,
        optimizer_kwargs={"lr": args.lr},
        dataloader_num_workers=0,
        log_on_step=True,
    )

    trainer = RL4COTrainer(
        accelerator="gpu" if device == "cuda" else "cpu",
        devices=1,
        max_epochs=args.max_epochs,
        precision="32-true",
        logger=False,
        enable_checkpointing=False,
        enable_progress_bar=False,
        num_sanity_val_steps=0,
        default_root_dir=str(CACHE_ROOT / "lightning"),
    )

    trainer.fit(model)
    print("training_ok: True")

    model.eval()
    model.to(device)
    infer_td = env.reset(sample_td.to(device))
    num_starts = env.get_num_starts(infer_td)
    with torch.inference_mode():
        out = model.policy(
            infer_td,
            env,
            phase="test",
            num_starts=num_starts,
            return_actions=True,
        )

    rewards = unbatchify(out["reward"], (1, num_starts)).squeeze(1)
    best_reward = rewards.max(dim=-1).values
    best_distance = -best_reward
    actions = unbatchify(out["actions"], (1, num_starts)).squeeze(1)
    first_best_idx = rewards[0].argmax()
    first_actions = actions[0, first_best_idx].detach().cpu().tolist()

    print("inference_ok: True")
    print(f"num_starts: {num_starts}")
    print(f"reward_shape: {tuple(rewards.shape)}")
    print(f"best_distance_mean: {best_distance.mean().item():.6f}")
    print(f"first_instance_best_distance: {best_distance[0].item():.6f}")
    print(f"first_instance_best_actions: {first_actions}")


if __name__ == "__main__":
    main()
